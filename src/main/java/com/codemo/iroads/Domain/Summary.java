package com.codemo.iroads.Domain;

public class Summary {

    private int journeyCount;
    private int taggedJourneyCount;

    public int getJourneyCount() {
        return journeyCount;
    }

    public void setJourneyCount(int journeyCount) {
        this.journeyCount = journeyCount;
    }

    public int getTaggedJourneyCount() {
        return taggedJourneyCount;
    }

    public void setTaggedJourneyCount(int taggedJourneyCount) {
        this.taggedJourneyCount = taggedJourneyCount;
    }
}
